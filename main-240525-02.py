# 사칙연산

num_1 = 3
num_2 = 4

# 더하기
print(num_1 + num_2)
# 곱하기
print(num_1 * num_2)
# 빼기
print(num_1 - num_2)
# 나누기
print(num_1 / num_2)

# 제곱 연산자 : **

a = 3
b = 4
print(a ** b) # 3 * 3 * 3 * 3

# 나머지 연산자 : %
# 어던 값을 나누었을 때의 나머지 값을 반환한다.

a = 7
b = 3

c = a % b

print(c)

# 몫 연산자 : //
# 어떤 값을 나누었을 때의 몫을 반환한다.

a = 7
b = 4

c = a // b

print(c)


